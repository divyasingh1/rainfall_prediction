rain<-read.csv("rainfall.csv")
cloud<-read.csv("cloud_cover.csv")
prec<-read.csv("prec.csv")
temp<-read.csv("temp.csv")
vap<-read.csv("vap_pressure.csv")
wetday<-read.csv("wet_day_freq.csv")

head(rain)
rain<-filter(rain,YEAR<=2002,SD_Name=="EAST UTTAR PRADESH")
tail(rain)
rain<-select(rain,YEAR:DEC)
tail(rain)
head(rain)
rain<-melt(rain,c("YEAR"))
rain<-arrange(rain,YEAR)
head(rain)
rainjan<-filter(rain,variable=="JAN")
head(rainjan)

head(cloud)
cloud<-select(cloud,YEAR:DEC)
head(cloud)
cloud<-melt(cloud,c("YEAR"))
head(cloud)
cloudjan<-filter(cloud,variable=="Jan")
head(cloudjan)
cloudjan$variable="JAN"

head(prec)
prec<-select(prec,YEAR:DEC)
head(prec)
prec<-melt(prec,c("YEAR"))
head(prec)
precjan<-filter(prec,variable=="Jan")
head(precjan)
precjan$variable="JAN"


head(temp)
temp<-select(temp,Year:Dec)
head(temp)
temp<-filter(temp,Year>=1951)
temp<-melt(temp,c("Year"))
head(temp)
tempjan<-filter(temp,variable=="Jan")
head(tempjan)
tempjan$variable="JAN"

head(vap)
vap<-select(vap,YEAR:DEC)
head(vap)
vap<-melt(vap,c("YEAR"))
head(vap)
vapjan<-filter(vap,variable=="Jan")
head(vapjan)
vapjan$variable="JAN"
tail(vapjan)

head(wetday)
wetday<-select(wetday,YEAR:DEC)
head(wetday)
wetday<-melt(wetday,c("YEAR"))
head(wetday)
wetdayjan<-filter(wetday,variable=="Jan")
head(wetdayjan)
wetdayjan$variable="JAN"
tail(wetdayjan)

jan<-data.frame(YEAR=rainjan$YEAR,RAIN=rainjan$value,CLOUD=cloudjan$value,PREC=precjan$value,TEMP=tempjan$value,VAP=vapjan$value,WETDAY=wetdayjan$value)
write.csv(jan,"JAN.csv")
